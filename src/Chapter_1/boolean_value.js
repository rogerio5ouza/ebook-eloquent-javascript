/*
VALORES BOOLEANOS

COMPARAÇÃO

Aqui está uma maneira de produzir valores booleanos:
*/

console.log(3 > 2)
// true
console.log(3 < 2)
// false

// Strings podem ser comparadas da mesma maneira:

console.log("Java" < "JavaScript")

// Outros operadores semelhantes são >= (maior ou igual), <= (menor ou igual), == (igual a ) e != (diferente de):

console.log("Java" != "Kotlin")
// true

console.log("Apple" == "Orange");
// false

// Existe apenas um valor em JavaScript que não é igual a ele mesmo, que é NaN ("Not a Number"):

console.log(NaN == NaN);